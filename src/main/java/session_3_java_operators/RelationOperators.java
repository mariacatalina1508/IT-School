package session_3_java_operators;

public class RelationOperators {

    public static void main(String[] args){
        int a = 10, b = 20;

        System.out.println(a == b); //false - egual operator
        System.out.println(a != b); //true
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
